a = 10

-- if example
if (a < 20)
   then
      print("less than 20")
   end

print("a:", a)

a = 20
print("a is changed to:", a)

-- if ..else example
if (a < 20)
   then
      print("less than 20")
   else
      print("greater than 20")
   end

-- if .. elseif example
if (a == 10)
   then
      print("a is 10")
elseif (a == 20)
   then
      print("a is 20")
else
   print("its a:", a)
end

